        $templateClause = $row['shift_template_id'] === null
            ? 'shift_template_id IS NULL'
            : 'shift_template_id=' . (int)$row['shift_template_id'];
        $matches = seedFetchAll(
            $db,
            "SELECT id FROM shift_assignments
             WHERE user_id=? AND assignment_date=? AND start_datetime=? AND end_datetime=?
               AND source='manual' AND monthly_template_id IS NULL
               AND {$templateClause}
             ORDER BY id",
            'isss',
            [$userId, $row['date'], $row['start'], $row['end']]
        );
        foreach ($matches as $match) {
            $assignmentId = (int)$match['id'];
            $stmt = $db->prepare('DELETE FROM shift_assignments WHERE id=?');
            if (!$stmt) throw new RuntimeException($db->error);
            $stmt->bind_param('i', $assignmentId);
            if (!$stmt->execute()) throw new RuntimeException($stmt->error);
            $stmt->close();
            $deleted[] = $assignmentId;
        }
    }
    return $deleted;
}

function resolveSeedActor(mysqli $db, int $requestedActorId): ?int
{
    if ($requestedActorId > 0) {
        $actor = seedFetchOne($db, 'SELECT id FROM tracs_users WHERE id=? LIMIT 1', 'i', [$requestedActorId]);
        if (!$actor) seedFail('The requested --actor-id does not exist.');
        return (int)$actor['id'];
    }
    $actor = seedFetchOne($db, "
        SELECT u.id
        FROM tracs_users u
        JOIN tracs_roles r ON r.id=u.role_id
        WHERE r.slug='super_admin' AND u.status='active' AND u.is_active=1
        ORDER BY u.id
        LIMIT 1
    ");
    return $actor ? (int)$actor['id'] : null;
}

