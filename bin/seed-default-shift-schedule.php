        ], $desiredAssignments);
        $settings = [
            'seed_key' => DEFAULT_SHIFT_SEED_KEY,
            'generated_by' => 'seed_real_shift_schedule',
            'schedule_mode' => 'weekly_matrix',
            'matrix_version' => 2,
            'week_mapping' => 'calendar_date',
            'week_5_behavior' => 'repeat_week_4',
            'pattern_week_labels' => ['Week 1', 'Week 2', 'Week 3', 'Week 4'],
            'week_labels' => ['Week 1', 'Week 2', 'Week 3', 'Week 4', 'Week 5'],
            'calendar_week_count' => (int)ceil((int)$targetMonth->modify('last day of this month')->format('j') / 7),
            'day_names' => ['Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'Saturday', 'Sunday'],
            'day_indexes' => [
                'Monday' => 1,
                'Tuesday' => 2,
                'Wednesday' => 3,
                'Thursday' => 4,
                'Friday' => 5,
                'Saturday' => 6,
                'Sunday' => 7,
            ],
            'agent_ids' => array_values(array_map(static fn(array $row): int => (int)$row['id'], $userMap)),
            'shift_hours' => [
                'Shift 1' => '00:00-08:00',
                'Shift 2' => '08:00-16:00',
                'Shift 3' => '16:00-24:00',
            ],
            'weekly_matrix' => $weeklyMatrix,
            'notes' => DEFAULT_SHIFT_SEED_NOTE,
        ];
        $settingsJson = json_encode($settings, JSON_UNESCAPED_SLASHES | JSON_UNESCAPED_UNICODE);
        if ($settingsJson === false) throw new RuntimeException("Unable to encode {$monthCode} template settings.");

        $template = seededTemplateForMonth($conn, $divisionId, $targetDate);
        $templateName = DEFAULT_SHIFT_TEMPLATE_NAME . ' - ' . $monthCode;
        if ($template) {
            $templateId = (int)$template['id'];
            $stmt = $conn->prepare("
                UPDATE shift_monthly_templates
                SET name=?,status='applied',settings_json=?,updated_by=?,updated_at=NOW(),archived_at=NULL,
                    applied_at=NOW(),applied_by=?
                WHERE id=?
            ");
            if (!$stmt) throw new RuntimeException($conn->error);
            $stmt->bind_param('ssiii', $templateName, $settingsJson, $actorId, $actorId, $templateId);
        } else {
            $stmt = $conn->prepare("
                INSERT INTO shift_monthly_templates
                  (name,division_id,target_month,status,settings_json,created_by,updated_by,
                   created_at,updated_at,applied_at,applied_by)
                VALUES (?, ?,?,'applied',?,?,?,NOW(),NOW(),NOW(),?)
            ");
            if (!$stmt) throw new RuntimeException($conn->error);
            $stmt->bind_param(
                'sissiii',
                $templateName,
                $divisionId,
                $targetDate,
                $settingsJson,
                $actorId,
                $actorId,
                $actorId
            );
        }
        if (!$stmt->execute()) throw new RuntimeException($stmt->error);
        if (!$template) $templateId = (int)$stmt->insert_id;
        $stmt->close();

        $foreignOwned = seedFetchOne(
            $conn,
            "SELECT id FROM shift_assignments
             WHERE monthly_template_id=? AND source<>'monthly_template'
             LIMIT 1",
            'i',
            [$templateId]
        );
        if ($foreignOwned) {
            throw new RuntimeException(
                "A {$monthCode} seeded assignment was converted to another source; refusing to replace it."
            );
        }

        foreach ($desiredAssignments as $row) {
            $conflict = seedFetchOne(
                $conn,
                "SELECT id,source,monthly_template_id
                 FROM shift_assignments
                 WHERE user_id=?
                   AND status NOT IN ('cancelled','replaced')
                   AND start_datetime<?
                   AND end_datetime>?
                   AND NOT (source='monthly_template' AND monthly_template_id=?)
                 LIMIT 1",
                'issi',
                [$row['agent_id'], $row['end_datetime'], $row['start_datetime'], $templateId]
            );
            if ($conflict) {
                throw new RuntimeException(
                    "Protected assignment conflict for {$row['agent_name']} on {$row['assignment_date']} "
                    . "{$row['shift_name']} (existing assignment #{$conflict['id']})."
                );
            }
        }

        $stmt = $conn->prepare("DELETE FROM shift_assignments WHERE monthly_template_id=? AND source='monthly_template'");
        if (!$stmt) throw new RuntimeException($conn->error);
        $stmt->bind_param('i', $templateId);
        if (!$stmt->execute()) throw new RuntimeException($stmt->error);
        $stmt->close();
        $stmt = $conn->prepare('DELETE FROM shift_monthly_template_items WHERE template_id=?');
        if (!$stmt) throw new RuntimeException($conn->error);
        $stmt->bind_param('i', $templateId);
        if (!$stmt->execute()) throw new RuntimeException($stmt->error);
        $stmt->close();

        $itemStmt = $conn->prepare("
            INSERT INTO shift_monthly_template_items
              (template_id,agent_id,shift_template_id,assignment_date,start_time,end_time,
               break_minutes,assignment_type,notes,created_at)
            VALUES (?,?,?,?,?,?,0,'regular_shift',?,NOW())
        ");
        $assignmentStmt = $conn->prepare("
            INSERT INTO shift_assignments
              (user_id,division_id,shift_template_id,assignment_date,start_datetime,end_datetime,is_cross_day,
               break_minutes,calculated_duration_minutes,assignment_type,status,is_overtime,is_holiday_assignment,
               is_manual_duration_override,approval_status,source,monthly_template_id,notes,created_by,updated_by,
               created_at,updated_at)
            VALUES (?,?,?,?,?,?,?,0,480,'regular_shift','assigned',0,0,0,'not_required','monthly_template',?,?,?,?,
                    NOW(),NOW())
        ");
        $linkStmt = $conn->prepare('UPDATE shift_monthly_template_items SET generated_assignment_id=? WHERE id=?');
        if (!$itemStmt || !$assignmentStmt || !$linkStmt) throw new RuntimeException($conn->error);
        foreach ($desiredAssignments as $row) {
            $itemStmt->bind_param(
                'iiissss',
                $templateId,
                $row['agent_id'],
                $row['shift_template_id'],
                $row['assignment_date'],
                $row['start_time'],
                $row['end_time'],
                $row['notes']
            );
            if (!$itemStmt->execute()) throw new RuntimeException($itemStmt->error);
            $itemId = (int)$itemStmt->insert_id;

            $assignmentStmt->bind_param(
                'iiisssiisii',
                $row['agent_id'],
                $divisionId,
                $row['shift_template_id'],
                $row['assignment_date'],
                $row['start_datetime'],
                $row['end_datetime'],
                $row['is_cross_day'],
                $templateId,
                $row['notes'],
                $actorId,
                $actorId
            );
            if (!$assignmentStmt->execute()) throw new RuntimeException($assignmentStmt->error);
            $assignmentId = (int)$assignmentStmt->insert_id;

            $linkStmt->bind_param('ii', $assignmentId, $itemId);
            if (!$linkStmt->execute()) throw new RuntimeException($linkStmt->error);
        }
        $itemStmt->close();
        $assignmentStmt->close();
        $linkStmt->close();
        $totalAssignments += count($desiredAssignments);
        $templateCount++;
    }

    $conn->commit();
} catch (Throwable $e) {
    $conn->rollback();
    seedFail($e->getMessage());
}

