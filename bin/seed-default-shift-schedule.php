function seedFail(string $message): never
{
    fwrite(STDERR, "ERROR: {$message}\n");
    exit(1);
}

function loadSeedEnvironment(string $path): void
{
    foreach (['DB_HOST', 'DB_PORT', 'DB_NAME', 'DB_USER', 'DB_PASS'] as $key) {
        $value = getenv($key);
        if ($value !== false && $value !== '') {
            $_ENV[$key] = $value;
        }
    }
    if (!is_file($path)) return;
    foreach (file($path, FILE_IGNORE_NEW_LINES | FILE_SKIP_EMPTY_LINES) ?: [] as $line) {
        $line = trim($line);
        if ($line === '' || str_starts_with($line, '#') || !str_contains($line, '=')) continue;
        [$key, $value] = array_map('trim', explode('=', $line, 2));
        if ($key === '' || isset($_ENV[$key])) continue;
        $_ENV[$key] = trim($value, "\"'");
    }
}

function seedFetchOne(mysqli $db, string $sql, string $types = '', array $params = []): ?array
{
    $stmt = $db->prepare($sql);
    if (!$stmt) throw new RuntimeException($db->error);
    if ($types !== '') $stmt->bind_param($types, ...$params);
    if (!$stmt->execute()) throw new RuntimeException($stmt->error);
    $row = $stmt->get_result()->fetch_assoc();
    $stmt->close();
    return $row ?: null;
}

function seedFetchAll(mysqli $db, string $sql, string $types = '', array $params = []): array
{
    $stmt = $db->prepare($sql);
    if (!$stmt) throw new RuntimeException($db->error);
    if ($types !== '') $stmt->bind_param($types, ...$params);
    if (!$stmt->execute()) throw new RuntimeException($stmt->error);
    $rows = $stmt->get_result()->fetch_all(MYSQLI_ASSOC);
    $stmt->close();
    return $rows;
}

function seedTableExists(mysqli $db, string $table): bool
{
    $row = seedFetchOne(
        $db,
        'SELECT 1 AS found FROM information_schema.tables WHERE table_schema=DATABASE() AND table_name=? LIMIT 1',
        's',
        [$table]
    );
    return $row !== null;
}

function normalizeSeedIdentity(string $value): string
{
    $value = strtolower(trim($value));
    return preg_replace('/[^a-z0-9]+/', '', $value) ?: '';
}

function findCanonicalRole(array $roles, array $slugs, array $names): ?array
{
    foreach ($slugs as $slug) {
        foreach ($roles as $role) {
            if (strtolower((string)$role['slug']) === strtolower($slug)) return $role;
        }
    }
    $normalizedNames = array_map('normalizeSeedIdentity', $names);
    foreach ($roles as $role) {
        if (in_array(normalizeSeedIdentity((string)$role['name']), $normalizedNames, true)) return $role;
    }
    return null;
}

function findSeedUser(array $users, array $spec): ?array
{
    $matches = [];
    foreach ($users as $user) {
        $nameMatch = normalizeSeedIdentity((string)$user['name']) === normalizeSeedIdentity($spec['name']);
        $usernameMatch = strtolower(trim((string)$user['username'])) === $spec['username'];
        $emailMatch = strtolower(trim((string)$user['email'])) === $spec['email'];
        if ($nameMatch || $usernameMatch || $emailMatch) {
            $matches[(int)$user['id']] = $user;
        }
    }
    if (count($matches) > 1) {
        seedFail("Multiple existing users match {$spec['name']}; resolve the identity collision before seeding.");
    }
    return $matches ? array_values($matches)[0] : null;
}

