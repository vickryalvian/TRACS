function seedMainShiftTemplates(mysqli $db): array
{
    $result = [];
    foreach (desiredShiftTemplates() as $name => $values) {
        $rows = seedFetchAll($db, 'SELECT * FROM shift_templates WHERE shift_name=? ORDER BY id', 's', [$name]);
        if (count($rows) > 1) seedFail("Multiple shift templates are named {$name}; resolve duplicates before seeding.");
        if ($rows) {
            $id = (int)$rows[0]['id'];
            $stmt = $db->prepare("
                UPDATE shift_templates
                SET start_time=?,end_time=?,duration_minutes=?,default_break_minutes=0,is_cross_day=?,
                    color_label=?,default_assignment_type='regular_shift',count_as_work_hour=1,is_active=1,
                    notes=?,updated_at=NOW()
                WHERE id=?
            ");
            if (!$stmt) throw new RuntimeException($db->error);
            $stmt->bind_param(
                'ssiissi',
                $values['start_time'],
                $values['end_time'],
                $values['duration_minutes'],
                $values['is_cross_day'],
                $values['color_label'],
                $values['notes'],
                $id
            );
        } else {
            $stmt = $db->prepare("
                INSERT INTO shift_templates
                  (shift_name,start_time,end_time,duration_minutes,default_break_minutes,is_cross_day,
                   color_label,default_assignment_type,count_as_work_hour,is_active,notes,created_at,updated_at)
                VALUES (?,?,?, ?,0,?,?,'regular_shift',1,1,?,NOW(),NOW())
            ");
            if (!$stmt) throw new RuntimeException($db->error);
            $stmt->bind_param(
                'sssiiss',
                $name,
                $values['start_time'],
                $values['end_time'],
                $values['duration_minutes'],
                $values['is_cross_day'],
                $values['color_label'],
                $values['notes']
            );
        }
        if (!$stmt->execute()) throw new RuntimeException($stmt->error);
        if (!$rows) $id = (int)$stmt->insert_id;
        $stmt->close();
        $result[$name] = array_merge(['id' => $id, 'shift_name' => $name], $values);
    }
    return $result;
}

function buildDesiredAssignments(
    DateTimeImmutable $month,
    array $userMap,
    array $shiftMap
): array {
    $rows = [];
    $timezone = new DateTimeZone('Asia/Jakarta');
    $monthCode = $month->format('Y-m');
    $lastDate = $month->modify('last day of this month');
    for ($date = $month; $date <= $lastDate; $date = $date->modify('+1 day')) {
        $dayName = $date->format('l');
        $calendarWeekIndex = intdiv((int)$date->format('j') - 1, 7) + 1;
        $patternWeekIndex = min(4, $calendarWeekIndex);
        foreach (defaultWeeklySchedule()[$dayName] as $shiftName => $agentNames) {
            $shift = $shiftMap[$shiftName];
            foreach ($agentNames as $agentName) {
                $user = $userMap[$agentName] ?? null;
                if (!$user) seedFail("Missing seeded user {$agentName}.");
                $start = new DateTimeImmutable(
                    $date->format('Y-m-d') . ' ' . $shift['start_time'],
                    $timezone
                );
                $end = new DateTimeImmutable(
                    $date->format('Y-m-d') . ' ' . $shift['end_time'],
                    $timezone
                );
                if (!empty($shift['is_cross_day'])) $end = $end->modify('+1 day');
                $weekNote = $calendarWeekIndex === 5
                    ? 'Week 5 (repeats Week 4)'
                    : "Week {$patternWeekIndex}";
                $rows[] = [
                    'week_index' => $calendarWeekIndex,
                    'pattern_week_index' => $patternWeekIndex,
                    'week_label' => "Week {$calendarWeekIndex}",
                    'day_of_week' => $dayName,
                    'day_index' => (int)$date->format('N'),
                    'agent_id' => (int)$user['id'],
                    'agent_name' => $agentName,
                    'shift_template_id' => (int)$shift['id'],
                    'shift_name' => $shiftName,
                    'assignment_date' => $date->format('Y-m-d'),
                    'start_time' => $shift['start_time'],
                    'end_time' => $shift['end_time'],
                    'start_datetime' => $start->format('Y-m-d H:i:s'),
                    'end_datetime' => $end->format('Y-m-d H:i:s'),
                    'is_cross_day' => (int)$shift['is_cross_day'],
                    'notes' => DEFAULT_SHIFT_SEED_NOTE . " {$monthCode}; {$weekNote}; {$dayName}; {$shiftName}.",
                ];
            }
        }
    }
    usort($rows, static fn(array $left, array $right): int =>
        [$left['assignment_date'], $left['start_time'], $left['agent_name']]
        <=> [$right['assignment_date'], $right['start_time'], $right['agent_name']]
    );
    return $rows;
}

function parseSeedMonth(string $value, DateTimeZone $timezone, string $label): DateTimeImmutable
{
    if (!preg_match('/^\d{4}-\d{2}$/', $value)) seedFail("{$label} must use YYYY-MM format.");
    $month = DateTimeImmutable::createFromFormat('!Y-m-d', $value . '-01', $timezone);
    if (!$month || $month->format('Y-m') !== $value) seedFail("{$label} is invalid.");
    return $month;
}

function seedMonthRange(DateTimeImmutable $start, DateTimeImmutable $end): array
{
    if ($end < $start) seedFail('End month must be on or after start month.');
    $months = [];
    for ($month = $start; $month <= $end; $month = $month->modify('+1 month')) {
        $months[] = $month;
    }
    return $months;
}

function seededTemplateForMonth(mysqli $db, int $divisionId, string $targetDate): ?array
{
    $rows = seedFetchAll(
        $db,
        'SELECT id,name,status,settings_json FROM shift_monthly_templates
         WHERE division_id=? AND target_month=? ORDER BY id',
        'is',
        [$divisionId, $targetDate]
    );
    $owned = [];
    foreach ($rows as $row) {
        $settings = json_decode((string)$row['settings_json'], true);
        if (($settings['seed_key'] ?? '') === DEFAULT_SHIFT_SEED_KEY) $owned[] = $row;
    }
    if (count($owned) > 1) {
        seedFail("Multiple seeded default templates exist for {$targetDate}.");
    }
    return $owned[0] ?? null;
}

